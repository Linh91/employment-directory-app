export class Directory {
    public name: string;
    public age: number;
    public title: string;
    public address: string;
    public telephone: string;

    constructor(name: string, age: number, title: string, address: string, telephone: string) {
        this.name = name;
        this.age = age;
        this.title = title;
        this.address = address;
        this.telephone = telephone;
    }
}
